/*
    算术运算符：+ - * / %
    注意：在java中两个整数相除结果还是整数，要想得到小数，得有小数参与运算。
 */
public class Demo1 {
    public static void main(String[] args) {

        System.out.println(10 + 3);
        System.out.println(10 - 3);
        System.out.println(10 * 3);
        System.out.println(10 / 3);
        System.out.println(10 % 3);
        System.out.println(10.0 / 3);

    }
}
