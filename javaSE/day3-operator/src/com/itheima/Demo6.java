/*
    关系运算符：用于比较两个变量/字面量的大小，结果是一个boolean类型的值，成立为true，不成立为false。
        >、<、>=、<=、==、!=
 */
public class Demo6 {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
